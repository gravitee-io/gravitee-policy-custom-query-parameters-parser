import java.util.Map;
import org.junit.jupiter.params.ParameterizedTest;
import org.junit.jupiter.params.provider.MethodSource;

/**
 * @author Yann TAVERNIER (yann.tavernier at graviteesource.com)
 * @author GraviteeSource Team
 */
@GatewayTest(v2ExecutionMode = ExecutionMode.V4_EMULATION_ENGINE)
@DeployApi({ "/apis/v2/api.json", "/apis/v4/api.json" })
public class CustomQueryParametersParserPolicyV4IntegrationTest extends CustomQueryParametersParserPolicyV3IntegrationTest {

    @Override
    public void configureEntrypoints(Map<String, EntrypointConnectorPlugin<?, ?>> entrypoints) {
        entrypoints.putIfAbsent("http-proxy", EntrypointBuilder.build("http-proxy", HttpProxyEntrypointConnectorFactory.class));
    }

    @Override
    public void configureEndpoints(Map<String, EndpointConnectorPlugin<?, ?>> endpoints) {
        endpoints.putIfAbsent("http-proxy", EndpointBuilder.build("http-proxy", HttpProxyEndpointConnectorFactory.class));
    }

    @ParameterizedTest
    @MethodSource("provideArguments")
    public void should_preserve_query_params_and_extract_them_properly_v4_api(
        String query,
        MultiValueMap<String, String> expectedQueryParameters,
        HttpClient client
    ) throws Exception {
        executeTest("/test-v4", query, expectedQueryParameters, client);
    }
}
